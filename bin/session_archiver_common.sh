#!/usr/bin/env bash

codex_mdfy_default_archive_root() {
  printf '%s\n' "${HOME}/codex-session-archives"
}

codex_mdfy_settings_dir() {
  printf '%s\n' "${HOME}/.codex-mdfy"
}

codex_mdfy_settings_file() {
  printf '%s/session-archiver.env\n' "$(codex_mdfy_settings_dir)"
}

codex_mdfy_logs_dir() {
  printf '%s/logs\n' "$(codex_mdfy_settings_dir)"
}

codex_mdfy_resolve_path() {
  python3 - "$1" <<'PY'
from pathlib import Path
import sys

print(Path(sys.argv[1]).expanduser().resolve())
PY
}

codex_mdfy_load_settings() {
  local settings_file

  settings_file="$(codex_mdfy_settings_file)"
  if [[ -f "${settings_file}" ]]; then
    # shellcheck disable=SC1090
    source "${settings_file}"
  fi
}

codex_mdfy_prompt_archive_root() {
  local default_root
  local requested_root

  default_root="${CODEX_SESSION_ARCHIVE_ROOT:-$(codex_mdfy_default_archive_root)}"
  if [[ -t 0 ]]; then
    printf 'Markdown archive root [%s]: ' "${default_root}" >&2
    read -r requested_root || true
  else
    requested_root=""
  fi
  if [[ -z "${requested_root}" ]]; then
    requested_root="${default_root}"
  fi
  codex_mdfy_resolve_path "${requested_root}"
}

codex_mdfy_write_settings() {
  local archive_root
  local sync_repo_root
  local settings_dir
  local settings_file

  archive_root="$1"
  sync_repo_root="$2"
  settings_dir="$(codex_mdfy_settings_dir)"
  settings_file="$(codex_mdfy_settings_file)"

  mkdir -p "${settings_dir}"
  {
    printf '%s\n' '# Generated by codex-mdfy.'
    printf '%s\n' '# This file is read by the installer, hook runner, and git sync job.'
    printf 'export CODEX_SESSION_ARCHIVE_ROOT=%q\n' "${archive_root}"
    printf 'export CODEX_SESSION_SYNC_REPO_ROOT=%q\n' "${sync_repo_root}"
  } > "${settings_file}"
}

codex_mdfy_find_existing_ancestor() {
  local candidate_path

  candidate_path="$1"
  if [[ -z "${candidate_path}" ]]; then
    return 1
  fi

  while [[ ! -e "${candidate_path}" ]]; do
    local parent_path

    parent_path="$(dirname "${candidate_path}")"
    if [[ "${parent_path}" == "${candidate_path}" ]]; then
      return 1
    fi
    candidate_path="${parent_path}"
  done

  printf '%s\n' "${candidate_path}"
}

codex_mdfy_detect_git_repo_root() {
  local archive_root
  local lookup_root

  archive_root="$1"
  lookup_root="$(codex_mdfy_find_existing_ancestor "${archive_root}")" || return 1
  git -C "${lookup_root}" rev-parse --show-toplevel 2>/dev/null
}

codex_mdfy_assert_sync_repo_ready() {
  local archive_root
  local sync_repo_root

  archive_root="$1"
  sync_repo_root="$2"

  if [[ -z "${sync_repo_root}" ]]; then
    printf '%s\n' "The archive root must be inside an existing git working tree: ${archive_root}" >&2
    return 1
  fi

  if ! git -C "${sync_repo_root}" rev-parse --is-inside-work-tree >/dev/null 2>&1; then
    printf '%s\n' "Failed to validate git working tree: ${sync_repo_root}" >&2
    return 1
  fi

  if ! git -C "${sync_repo_root}" remote get-url origin >/dev/null 2>&1; then
    printf '%s\n' "The detected git repo does not have an origin remote: ${sync_repo_root}" >&2
    return 1
  fi
}

codex_mdfy_link_with_backup() {
  local source_path
  local target_path
  local source_realpath
  local target_realpath
  local backup_path
  local timestamp

  source_path="$1"
  target_path="$2"
  source_realpath="$(codex_mdfy_resolve_path "${source_path}")"

  mkdir -p "$(dirname "${target_path}")"

  if [[ -L "${target_path}" ]]; then
    target_realpath="$(codex_mdfy_resolve_path "${target_path}")"
    if [[ "${target_realpath}" == "${source_realpath}" ]]; then
      return 0
    fi
  fi

  if [[ -e "${target_path}" || -L "${target_path}" ]]; then
    timestamp="$(date +%Y%m%d%H%M%S)"
    backup_path="${target_path}.backup.${timestamp}"
    mv "${target_path}" "${backup_path}"
    printf 'Backed up %s to %s\n' "${target_path}" "${backup_path}" >&2
  fi

  ln -sfn "${source_realpath}" "${target_path}"
}

codex_mdfy_install_runtime_links() {
  local repo_root

  repo_root="$1"
  codex_mdfy_link_with_backup "${repo_root}/bin/run_session_archiver_hook.sh" "${HOME}/.codex-mdfy/run_session_archiver_hook.sh"
  codex_mdfy_link_with_backup "${repo_root}/bin/sync_archive_repo.sh" "${HOME}/.codex-mdfy/sync_archive_repo.sh"
  codex_mdfy_link_with_backup "${repo_root}/.codex/hooks.json" "${HOME}/.codex/hooks.json"
  codex_mdfy_link_with_backup "${repo_root}/skills/session-archiver" "${HOME}/.agents/skills/session-archiver"
}

codex_mdfy_remove_legacy_launcher() {
  local legacy_launcher

  legacy_launcher="${HOME}/.local/bin/codex-mdfy"
  if [[ -L "${legacy_launcher}" ]]; then
    rm -f "${legacy_launcher}"
    printf 'Removed legacy launcher: %s\n' "${legacy_launcher}" >&2
  fi
}

codex_mdfy_enable_hooks_feature() {
  if command -v codex >/dev/null 2>&1; then
    codex features enable codex_hooks >/dev/null
    return 0
  fi

  printf '%s\n' 'codex command not found. Install completed, but codex_hooks was not enabled automatically.' >&2
}

codex_mdfy_register_sync_cronjob() {
  local crontab_bin
  local crontab_dump_file
  local filtered_crontab_file
  local sync_script_path
  local sync_log_path
  local cron_line

  crontab_bin="${CODEX_MDFY_CRONTAB_BIN:-crontab}"
  if ! command -v "${crontab_bin}" >/dev/null 2>&1; then
    printf '%s\n' 'crontab command not found. Unable to register the daily git sync job.' >&2
    return 1
  fi

  mkdir -p "$(codex_mdfy_logs_dir)"

  sync_script_path="${HOME}/.codex-mdfy/sync_archive_repo.sh"
  sync_log_path="$(codex_mdfy_logs_dir)/git-sync.log"
  cron_line="0 3 * * * /usr/bin/env bash \"${sync_script_path}\" >> \"${sync_log_path}\" 2>&1"

  crontab_dump_file="$(mktemp)"
  filtered_crontab_file="$(mktemp)"

  "${crontab_bin}" -l > "${crontab_dump_file}" 2>/dev/null || true

  awk '
    $0 == "# BEGIN codex-mdfy daily git sync" {skip = 1; next}
    $0 == "# END codex-mdfy daily git sync" {skip = 0; next}
    skip == 0 && index($0, ".codex-mdfy/sync_archive_repo.sh") == 0 {print}
  ' "${crontab_dump_file}" > "${filtered_crontab_file}"

  {
    cat "${filtered_crontab_file}"
    printf '%s\n' '# BEGIN codex-mdfy daily git sync'
    printf '%s\n' "${cron_line}"
    printf '%s\n' '# END codex-mdfy daily git sync'
  } | "${crontab_bin}" -

  rm -f "${crontab_dump_file}" "${filtered_crontab_file}"
}
